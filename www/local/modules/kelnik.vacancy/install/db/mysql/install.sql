    `PRICE_MAX` INT(11) UNSIGNED NOT NULL DEFAULT '0',
    `ACTIVE` ENUM('Y','N') NOT NULL DEFAULT 'N',
    `NAME` VARCHAR(255) NOT NULL,
    `RESIDENT_ID` INT(11) UNSIGNED NOT NULL DEFAULT '0',
    `PRICE_TEXT` VARCHAR(255) NULL DEFAULT NULL,
    `EXPERIENCE` VARCHAR(255) NULL DEFAULT NULL,
    `EMPLOYMENT` VARCHAR(255) NULL DEFAULT NULL,
    `CONTACTS` VARCHAR(255) NULL DEFAULT NULL,
    `DESCR_TEXT_TYPE` VARCHAR(4) NULL DEFAULT 'html',
    `DUTIES_TEXT_TYPE` VARCHAR(4) NULL DEFAULT 'html',
    `REQUIREMENTS_TEXT_TYPE` VARCHAR(4) NULL DEFAULT 'html',
    `CONDITIONS_TEXT_TYPE` VARCHAR(4) NULL DEFAULT 'html',
    `DESCR` TEXT NULL,
    `DUTIES` TEXT NULL,
    `REQUIREMENTS` TEXT NULL,
    `CONDITIONS` TEXT NULL,
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8 ROW_FORMAT=COMPACT;
